function checkPhoneNmbr(inputS)
// ??????? ?? ?????? ??? ???????, ?? ?????????? ???????,
// ? ?????????? ?????????? ??????.
  {
    var sample = '0123456789';
    var outputS = '';
    var c;

    for ( i=0; i<inputS.length; i++ )
    {
      c = inputS.substr(i,1);

      if ( sample.indexOf(c)>=0 )
        outputS = outputS + c;
    };

    return(outputS);
  };